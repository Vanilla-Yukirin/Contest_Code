#define N 1010

int h,w,n;
string str;
bool m[N][N];
int ans;

void nodfs(int x,int y,int step)
{
	if(m[x][y]==0) return;
	
	
	
	
	if(step==(int)str.size())
	{
		ans++;
		
		return;
	}
	
	if(str[step]=='U') nodfs(x-1,y,step+1);
	if(str[step]=='D') nodfs(x+1,y,step+1);
	if(str[step]=='L') nodfs(x,y-1,step+1);
	if(str[step]=='R') nodfs(x,y+1,step+1);
	
	
	
}

