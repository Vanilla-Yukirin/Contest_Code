#define N 1000010
double R;
double eps=1e-6;
void solve()
{
	cin>>R;
	LL ans=0;
	for(int i=0;i<=R;i++)
	{
		//calc up line
		double y=i+0.5;
		double len2=R*R-y*y;
		if(len2<-eps) continue;
		double len=sqrt(len2);
		LL now=0;
		if(len>=0.5-eps) now++,len-=0.5;
		now+=2*(LL)(len);
		if(i==0) ans+=now;
		else ans+=2*now;
	}
	cout<<ans;
}

